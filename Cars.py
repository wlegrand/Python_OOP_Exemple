

class Cars:

    def __init__(self, brand, color, power, model):
        self.brand = brand
        self.color = color
        self.power = power
        self.model = model

    def get_color(self):
        return self.color

    def get_brand(self):
        return self.brand

    def get_power(self):
        return self.power

    def get_model(self):
        return self.model

